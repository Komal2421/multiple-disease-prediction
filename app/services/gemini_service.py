import os
import sys
import traceback
from google import genai
from dotenv import load_dotenv

load_dotenv(override=True)

GEMINI_API_KEY = os.getenv("GEMINI_API_KEY")

gemini_client = None
if not GEMINI_API_KEY:
    print("CRITICAL CONFIGURATION ERROR: The 'GEMINI_API_KEY' environment variable is missing or empty! Gemini AI recommendations will be disabled.")
else:
    try:
        gemini_client = genai.Client(api_key=GEMINI_API_KEY)
        print("Gemini GenAI SDK client initialized successfully.")
    except Exception as e:
        print(f"CRITICAL INITIALIZATION ERROR: Failed to initialize the Gemini SDK: {e}")

def generate_ai_explanation(disease_name, result, risk_level, confidence, features):
    """
    Generates personalized clinical explanations using Google Gemini GenAI SDK.
    Supports dynamic fallback across candidate models if rate limits or errors occur.
    """
    if not gemini_client:
        return "AI explanation is unavailable because the GEMINI_API_KEY is not configured or failed to initialize on the server. Please set a valid API key to activate this feature."
    
    feature_details = "\n".join([f"- **{k}**: {v}" for k, v in features.items()])
    
    prompt = f"""
You are an expert medical AI assistant. Explain the following clinical prediction results generated by a machine learning classifier.

Disease Name: {disease_name}
ML Classifier Prediction: {result}
Calculated Risk Level: {risk_level}
Confidence of Classifier: {confidence}%

Patient Inputs Received:
{feature_details}

Please structure your explanation as follows:
1. **Understanding the Risk Assessed**: Explain what the predicted outcome and the {confidence}% confidence mean for the patient in plain English.
2. **Analysis of Key Metrics**: Walk through the clinical significance of the patient's inputs. Specifically highlight how values like BMI, glucose, blood pressure, etc. (where applicable) could influence risk.
3. **Wellness Guidance & Preventive Action**: Outline 3-4 actionable lifestyle or wellness recommendations (dietary changes, symptom monitoring, etc.) tailored to this prediction.
4. **Official Disclaimer**: Explicitly state that this tool is a predictive model, not a doctor. Advise them to seek professional medical advice before making health decisions.

Make your response clean, professional, and empathetic, formatted in markdown with bullet points and bold highlights.
"""
    candidate_models = [
        "gemini-2.0-flash",
        "gemini-2.0-flash-lite",
        "gemini-3.5-flash",
        "gemini-3.1-flash-lite",
        "gemini-flash-latest",
        "gemini-flash-lite-latest"
    ]
    
    last_error = None
    for model_name in candidate_models:
        try:
            print(f"[Gemini AI] Attempting explanation generation using model: {model_name}...")
            response = gemini_client.models.generate_content(
                model=model_name,
                contents=prompt
            )
            print(f"[Gemini AI] SUCCESS: Generated explanation using model: {model_name}")
            return response.text
        except Exception as e:
            last_error = e
            print(f"[Gemini AI] WARNING: Failed to generate using model '{model_name}': {e}", file=sys.stderr)
            
    print("\n[Gemini AI] CRITICAL: All fallback models failed to generate content. Last exception traceback:", file=sys.stderr)
    traceback.print_exc(file=sys.stderr)
    
    err_msg = str(last_error)
    if "429" in err_msg or "RESOURCE_EXHAUSTED" in err_msg:
        return (
            "**Gemini AI is temporarily unavailable** due to API rate limits on the free tier. "
            "The explanation could not be generated right now. Please wait a moment and try again, "
            "or consider upgrading your Gemini API plan at [ai.google.dev](https://ai.google.dev)."
        )
    return f"Failed to generate AI explanation. Error details: {err_msg}"
